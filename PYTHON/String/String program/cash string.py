#Write a program to display a string from the user and display it in the upper case and take other string and display it in lowercase.


string1 = input("Enter the first string: ")
uppercase_string1 = string1.upper()
print("Uppercase String 1:", uppercase_string1)

    
string2 = input("Enter the second string: ")

lowercase_string2 = string2.lower()
print("Lowercase String 2:", lowercase_string2)