# Write the program to calculate and display the total amount payable by Ananya:Ananya purchased 5 pencils and 2 erasers at the cost of Rs 7 and Rs 5 respectively.

pencils = 5
erasers = 2

cost_pencil = 7
cost_eraser = 5


total_amount_payable = (pencils * cost_pencil) + (erasers * cost_eraser)

print("Ananya purchased {} pencils and {} erasers.".format(pencils,erasers))
print("Total amount payable by Ananya: Rs {}" .format(total_amount_payable))

