# Display the month as per the choice from 1 to 12

month = int(input("Enter a number from 1 to 12: "))

if month == 1:
    print(month,"for January")
elif month == 2:
    print(month,"for February")
elif month == 3:
    print(month,"for March")
elif month == 4:
    print(month,"for April")
elif month == 5:
    print(month,"for May")
elif month == 6:
    print(month,"for June")
elif month == 7:
    print(month,"for July")
elif month == 8:
    print(month,"for August")
elif month == 9:
    print(month,"for September")
elif month == 10:
    print(month,"for October")
elif month == 11:
    print(month,"for November")
elif month == 12:
    print(month,"for December")
else:
    print("Invalid input,Please enter a number from 1 to 12.")